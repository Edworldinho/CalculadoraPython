class CalculadoraPython:
    
    def __init__(self,a,b):
        self.a = a
        self.b = b

    def somar(self):
        return self.a + self.b
    
    def diminuir(self):
        return self.a - self.b
    
    def multiplicar(self):
        return self.a * self.b
    
    def dividir(self):
        return self.a / self.b
